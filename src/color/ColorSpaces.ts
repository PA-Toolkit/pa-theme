/**
 * The RGB object.
 */
export interface RGB {
  /**
   * Red property of RGB.
   */
  red: number;
  /**
   * Green property of RGB.
   */
  green: number;
  /**
   * Blue property of RGB.
   */
  blue: number;
}
/**
 * The HSL object.
 */
export interface HSL {
  /**
   * Hue property of HSL.
   */
  hue: number;
  /**
   * Saturation property of HSL.
   */
  saturation: number;
  /**
   * Lightness property of HSL.
   */
  lightness: number;
}
